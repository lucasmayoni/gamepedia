CREATE SCHEMA IF NOT EXISTS gp;

CREATE TABLE IF NOT EXISTS gp.GAME (
  ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  TITLE VARCHAR(50) NOT NULL,
  DESCRIPTION VARCHAR(100) NOT NULL ,
  PLATFORM VARCHAR(20) ,
  RELEASE_DATE DATE
);

CREATE TABLE IF NOT EXISTS gp.GAME_REVIEW (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    GAME_ID BIGINT,
    REVIEWER VARCHAR(30),
    REVIEW_DATE DATE,
    REVIEW VARCHAR(200)
)